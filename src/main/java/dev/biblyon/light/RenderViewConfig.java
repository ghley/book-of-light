package dev.biblyon.light;

public class RenderViewConfig {
    public static final RenderViewConfig DEFAULT = new RenderViewConfig();

    private String frameTitle = "Book of Light";
    private int width = 1280;
    private int height = 720;
    private boolean disableCursor = false;

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public String getFrameTitle() {
        return frameTitle;
    }

    public void setFrameTitle(String frameTitle) {
        this.frameTitle = frameTitle;
    }

    public boolean isDisableCursor() {
        return disableCursor;
    }

    public void setDisableCursor(boolean disableCursor) {
        this.disableCursor = disableCursor;
    }
}
